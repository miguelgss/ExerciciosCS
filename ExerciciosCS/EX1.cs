using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ExerciciosCS
{
    class EX1
    {
        /// <summary>
        /// Escreva um programa que exiba os números de 1 a 1000 na tela em ordem
        /// decrescente.
        /// </summary>
        public static void ex1()
        {
            for (int i = 1000; i > 0; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
